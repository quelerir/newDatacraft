const MIN_TOPIC_MESSAGES = 15;

function resolveTopicScenario(agent: Pick<MockAgent, 'folderId'>, topic: Pick<MockAgentTopic, 'title' | 'preview' | 'prompt'>) {
  const text = `${topic.title} ${topic.preview} ${topic.prompt}`.toLowerCase();

  if (agent.folderId === 'growth') return 'growth';
  if (agent.folderId === 'data') return 'data';
  if (agent.folderId === 'leadership') return 'leadership';
  if (agent.folderId === 'alerts') return 'alerts';
  if (agent.folderId === 'partners') return 'partners';
  if (agent.folderId === 'vendors') return 'vendors';
  if (agent.folderId === 'research') return 'research';
  if (text.includes('retention') || text.includes('churn')) return 'retention';
  if (text.includes('api') || text.includes('коннектор')) return 'integration';

  return 'generic';
}

