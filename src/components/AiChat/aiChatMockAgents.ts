function ensureTopicConversationDepth(agent: Pick<MockAgent, 'folderId'>, topic: MockAgentTopic): MockAgentTopic {
  if (topic.messages.length >= MIN_TOPIC_MESSAGES) {
    return topic;
  }

  const extensionPool = buildTopicExtensionPool(agent, topic);
  const nextMessages = [...topic.messages];
  let poolIndex = 0;

  while (nextMessages.length < MIN_TOPIC_MESSAGES) {
    const candidate = extensionPool[poolIndex % extensionPool.length]!;
    const lastRole = nextMessages.at(-1)?.role;

    if (candidate.role !== lastRole) {
      nextMessages.push(candidate);
    }

    poolIndex += 1;
  }

  return {
    ...topic,
    messages: nextMessages,
  };
}

function pushTag(tags: string[], value: string) {
  if (!tags.includes(value)) {
    tags.push(value);
  }
}

function buildTopicTags(agent: Pick<MockAgent, 'id' | 'folderId'>, topic: Pick<MockAgentTopic, 'title' | 'preview' | 'prompt'>) {
  const tags: string[] = [];
  const text = `${topic.title} ${topic.preview} ${topic.prompt}`.toLowerCase();

  const baseTagMap: Record<string, string> = {
    growth: 'Growth',
    data: 'Data',
    leadership: 'Руководство',
    alerts: 'Алерты',
    partners: 'Партнёры',
    vendors: 'Интеграции',
    research: 'Research',
  };

  if (baseTagMap[agent.folderId]) {
    pushTag(tags, baseTagMap[agent.folderId]!);
  }

  if (text.includes('retention')) pushTag(tags, 'Retention');
  if (text.includes('конверс') || text.includes('cr')) pushTag(tags, 'Конверсия');
  if (text.includes('cac') || text.includes('бюджет')) pushTag(tags, 'Экономика');
  if (text.includes('дашборд') || text.includes('выруч')) pushTag(tags, 'Метрики');
  if (text.includes('качест') || text.includes('sla')) pushTag(tags, 'Качество');
  if (text.includes('инцидент') || text.includes('risk') || text.includes('риск')) pushTag(tags, 'Риски');
  if (text.includes('анома') || text.includes('алерт')) pushTag(tags, 'Аномалии');
  if (text.includes('api') || text.includes('интеграц') || text.includes('коннектор')) pushTag(tags, 'API');
  if (text.includes('board') || text.includes('руковод')) pushTag(tags, 'Отчёт');
  if (text.includes('pilot') || text.includes('пилот')) pushTag(tags, 'Пилот');
  if (text.includes('eval') || text.includes('эксперимент') || text.includes('гипотез')) pushTag(tags, 'Эксперименты');

  return tags.slice(0, 3);
}

